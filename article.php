            <div class="article-content">
                <p class="article-lead">World leaders from over 150 countries gathered at the International Climate Summit in Geneva this week to discuss urgent action on rising global temperatures, with many calling it the "last chance" to prevent irreversible climate damage.</p>

                <p>The summit, which marks the most significant climate gathering since the Paris Agreement of 2015, comes amid growing global concern as extreme weather events continue to devastate communities worldwide and new scientific reports indicate acceleration in polar ice melt beyond previous projections.</p>

                <p>"We stand at a critical juncture in human history," said UN Secretary-General Ant√≥nio Guterres in his opening address. "The decisions we make today will echo through generations. We cannot afford further delay or half-measures."</p>

                <h2>Ambitious New Targets</h2>

                <p>Many countries arrived with enhanced climate commitments. China announced plans to peak its carbon emissions by 2027, three years earlier than previously pledged. The European Union revealed a new target of reducing emissions by 65% by 2030 compared to 1990 levels, up from the previous 55% target.</p>

                <p>The United States, represented by President Trump in his first major international appearance since returning to office, surprised observers by maintaining the climate commitments established during the previous administration, despite campaign rhetoric suggesting a potential withdrawal from international climate agreements.</p>

                <blockquote>
                    "America will continue to lead through innovation, not regulation. We can protect our planet without destroying our economy," Trump stated during his address to delegates.
                </blockquote>

                <div class="article-image-inline">
                    <img src="/api/placeholder/800/400" alt="Protest outside climate summit">
                    <figcaption>Climate activists demonstrate outside the summit venue. Photo: AFP</figcaption>
                </div>

                <h2>Financial Commitments</h2>

                <p>A major focus of the summit has been climate finance, with developing nations emphasizing the need for substantial financial support to transition to clean energy and adapt to climate impacts already being experienced.</p>

                <p>A new climate finance package was unveiled, with industrialized nations collectively pledging $200 billion annually by 2027 to support climate initiatives in developing countries. This represents a significant increase from the previous $100 billion annual target, which many observers note has not been consistently met.</p>

                <p>"This financial commitment is essential, but the true test will be in the implementation," said Nigerian Environment Minister Amina Mohammed. "Africa contributes minimally to global emissions yet suffers disproportionately from climate impacts."</p>

                <h2>Corporate Involvement</h2>

                <p>For the first time, major corporations were formally included in the summit proceedings, with CEOs from over 50 global companies participating in dedicated sessions.</p>

                <p>A coalition of financial institutions representing over $70 trillion in assets announced new standards for climate-related disclosures and investment criteria, signaling a major shift in how global capital markets approach climate risk.</p>

                <p>"The business case for climate action has never been clearer," said Maryam Al-Mazrouei, CEO of Global Sustainable Investments. "Companies that fail to adapt will simply not remain competitive in tomorrow's economy."</p>

